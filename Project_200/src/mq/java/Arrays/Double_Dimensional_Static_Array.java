package mq.java.Arrays;

public class Double_Dimensional_Static_Array {

	public static void main(String[] args) {
		
		
		String data[][]=new String[3][2];
		
		//1st row data
		data[0][0]="iphone";
		data[0][1]="64GB";
		
		//2nd row data
		data[1][0]="samsung";
		data[1][1]="64GB";
		
		//3rdrow data
		data[2][0]="OnePlus";
		data[2][1]="64GB";
		
		
		System.out.println(data[1][0]);
		

	}

}
